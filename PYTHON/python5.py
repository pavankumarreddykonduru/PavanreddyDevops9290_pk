words = ["I", "am", "learning", "Devops"]
result = " ".join(words)
print(result)


""" Explanation

words is a list of strings:

["I", "am", "learning", "Devop"]
" ".join(words):
" " → separator (space)
.join(words) → combines all list elements into one string

So it becomes:

"I am learning Devop" """
